    private void combo_kategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_kategoriActionPerformed
        // TODO add your handling code here:
        String combo = combo_kategori.getSelectedItem().toString();
        String split[] = combo.split(" ");
        String kodekategori = split[0];
        String bantu;
        int kode;
        try {
            kode = Barang_Kontrol.getkoneksibarang().kodeOtomatisCekBarang(kodekategori) + 1;
            if (kode == 1) {
                bantu = "001";
                teks_kode_barang.setText(kodekategori + bantu);
            } else if (kode < 10) {
                bantu = "00";
                teks_kode_barang.setText(kodekategori + bantu + Integer.toString(kode));
            } else if (10 <= kode && kode < 100) {
                bantu = "0";
                teks_kode_barang.setText(kodekategori + bantu + Integer.toString(kode));
            } else {
                teks_kode_barang.setText(kodekategori + Integer.toString(kode));
            }
        } catch (SQLException ex) {
            Logger.getLogger(GUIBarang.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_combo_kategoriActionPerformed

