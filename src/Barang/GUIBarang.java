    private void tombol_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombol_simpanActionPerformed
        // TODO add your handling code here:
        if (teks_nama_barang.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Silahkan isi nama barang");
        } else {
            Barang brg = new Barang();
            brg.setImage(teks_kode_barang.getText());
            brg.setKodebarang(teks_kode_barang.getText());
            brg.setNamabarang(teks_nama_barang.getText());
            String[] split = combo_kategori.getSelectedItem().toString().split(" ");
            Kategori kat = new Kategori();
            kat.setKode(split[0]);
            brg.setKategori(kat);
            brg.setSatuan(combo_satuan.getSelectedItem().toString());
            brg.setImage(label_alamat.getText());

            if (teks_qty_min.getText().equals("")) {
                brg.setQtymin(0);
            } else {
                brg.setQtymin(new Integer(teks_qty_min.getText()));
            }
            if (teks_qty_max.getText().equals("")) {
                brg.setQtymax(0);
            } else {
                brg.setQtymax(new Integer(teks_qty_max.getText()));
            }
            if (teks_beli_sebelum.getText().equals("")) {
                brg.setBeliSebelum(0);
            } else {
                brg.setBeliSebelum(new Double(teks_beli_sebelum.getText()));
            }
            if (teks_beli_setelah.getText().equals("")) {
                brg.setBeliSesudah(0);
            } else {
                brg.setBeliSesudah(new Double(teks_beli_setelah.getText()));
            }
            if (teks_ritel_sebelum.getText().equals("")) {
                brg.setRitelSebelum(0);
            } else {
                brg.setRitelSebelum(new Double(teks_ritel_sebelum.getText()));
            }
            if (teks_ritel_setelah.getText().equals("")) {
                brg.setRitelSesudah(0);
            } else {
                brg.setRitelSesudah(new Double(teks_ritel_setelah.getText()));
            }
            if (!label_alamat.getText().equals("")) {
                    try {
                        new NIOCopier(label_alamat.getText(), gambar(teks_kode_barang.getText()));
                    } catch (IOException ex) {
                        Logger.getLogger(GUIBarang.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            brg.setImage(teks_kode_barang.getText());

            if (brg.getQtymax() >= brg.getQtymin()) {
                try {
                    Barang_Kontrol bk = Barang_Kontrol.getkoneksibarang();

                    if (tombol_simpan.getText().equals("Update")) {
                        bk.updateBarang(brg);
                        JOptionPane.showMessageDialog(null, "Data terupdate");
                    } else {
                        bk.tambahBarang(brg);
                        JOptionPane.showMessageDialog(null, "Data tersimpan");
                    }

                    bersihkanteks();
                } catch (SQLException ex) {
                    Logger.getLogger(GUIBarang.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Stok maksimal tidak boleh lurang dari stok minimal");
            }
        }

    }

